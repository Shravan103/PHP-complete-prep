<!-- Switch case statement -->
<?php
    $age = 45;
    switch($age)
    {
        case 12:
            echo "You are 12yrs old<br>";
            break;
    
        case 45:
            echo "You are 45 yrs old<br>";
            break;

        case 56:
            echo "You are 56 yrs old<br>";
            break;
    
        default:
            echo "Your age is wrong!<br>";
            break;
    }
?>